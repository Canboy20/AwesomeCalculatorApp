package com.test.awesomemath;

public class AwesomeCalculator {

    public static int addTwoAwesomeNumbers(int num1, int num2) {

        return num1 + num2;
    }

    public static int addThreeAwesomeNumbers(int num1, int num2, int num3) {

        return num1 + num2 + num3;
    }

    public static int multiplyTwoAwesomeNumbers(int num1, int num2) {

        return num1 * num2;
    }

    public static int multiplyThreeAwesomeNumbers(int num1, int num2, int num3) {

        return num1 * num2 * num3;
    }

}
